موقع لمسة : هو موقع الكتروني يقوم بربط محلات مدينة قلقيلية بالموقع ، ومن ثم يقوم كل محل بعرض بضاعته على الموقع والسماح للمستخدمين بالتصفح و شراء البضائع من الموقع وامكانية تسليمها للمستخدمين حسب رغبة المستخدم


<br>




<h3>الالية(للحساب الشخصي):</h3>
<li>عند التسجيل في الموقع يمتلك كل مستخدم حساب له في الموقع  </li>
<li>تم تقسيم مدينة قلقيلية الى عدة مناطق لتسهيل عملية توصيل الطلبات   </li>
<li>يقوم كل مستخدم بالتسجيل في المنطقة التي تتضمن الحي او الشارع الذي يسكن فيه  </li>
<li>يقوم المستخدم بشحن مبلغ من المال في حسابه عن طريق نقاط الشحن الموجودة في كل المناطق او عن طريق تقديم طلب شحن رصيد </li>من الموقع نفسه </li>
<li>يقوم المستخدم باستكشاف المحلات المتواجدة ضمن منطقته </li>
<li>يقوم المستخدم بشراء ما يحتاج من البضاعة ، بحيث تتواجد عملية الشراء التي يقوم بها المستخدم في سلة المشتريات  </li>
<li>يتم توصيل طلبات المستخدمين يومين من كل اسبوع (يوم التسليم) </li>
<li>عند تنفيذ أي عملية شراء يقوم الموقع تلقائيا بخصم قيمة الشراء وارسالها الى حساب صاحب المحل  </li>
</ol>


