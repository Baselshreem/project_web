<h3>: الشراء من الموقع</h3>
<li>الشراء (بدون خدمة توصيل): حيث يقوم المستخدم بالدخول الى صفحة المحل الذي يريده ويقوم بالشراء ، ويتم تحديد موعد استلام البضاعة ومن ثم يذهب المستخدم لاستلام البضاعة فقط دون دفع أي مبلغ للمحل
 </li>
<li>الشراء (مع خدمة توصيل) : حيث يقوم المستخدم المستخدم بالدخول الى صفحة المحل الذي يريده ويقوم بالشراء ، ومن ثم يقوم الموقع بارسال تكسي لاخذ البضاعة من المحل وتوصيلها الى المستخدم
  </li>
 
</ol>

